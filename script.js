function createChart(ctx, label, data, color) {
  const gradient = ctx.getContext('2d').createLinearGradient(0, 0, 0, 300);
  gradient.addColorStop(0, color + '88'); // semi-transparent top
  gradient.addColorStop(1, color + '22'); // more transparent bottom

  return new Chart(ctx, {
    type: 'line',
    data: {
      labels: timestamps,
      datasets: [{
        label: label,
        data: data,
        borderColor: color,
        backgroundColor: gradient,
        fill: true,
        tension: 0.4,
        pointRadius: 4,
        pointHoverRadius: 8
      }]
    },
    options: {
      responsive: true,
      interaction: {
        mode: 'nearest',
        intersect: false
      },
      plugins: {
        legend: {
          display: true,
          position: 'top',
          labels: {
            font: { size: 16 },
            color: '#C9DDE5' // legend text light
          }
        },
        tooltip: {
          enabled: true,
          backgroundColor: '#313366', // dark tooltip background
          titleColor: '#C9DDE5',      // title light
          bodyColor: '#C9DDE5',       // body light
          padding: 10,
          cornerRadius: 8,
          displayColors: false,
          titleFont: { size: 16 },
          bodyFont: { size: 14 }
        }
      },
      scales: {
        x: {
          title: {
            display: true,
            text: 'Time (HH:MM)',
            font: { size: 16 },
            color: '#C9DDE5' // x-axis title light
          },
          ticks: { font: { size: 14 }, color: '#C9DDE5' },
          grid: { color: '#313366' } // x-axis grid lines
        },
        y: {
          title: {
            display: true,
            text: label,
            font: { size: 16 },
            color: '#C9DDE5' // y-axis title light
          },
          ticks: { font: { size: 14 }, color: '#C9DDE5' },
          grid: { color: '#313366' } // y-axis grid lines
        }
      }
    }
  });
}

